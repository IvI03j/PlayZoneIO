export const getGamesByCategory = (category: string) => {
  if (category === 'all') return games;
  return games.filter(game => game.category === category);
};

export const getGameById = (id: number) => {
  return games.find(game => game.id === id);
};

export const slugify = (value: string) => {
  return value
    .toLowerCase()
    .normalize('NFD')
    .replace(/[^\w\s-]/g, '')
    .trim()
    .replace(/\s+/g, '-')
    .replace(/-+/g, '-');
};

export const getGameSlug = (game: Game) => {
  return `jugar-${slugify(game.title)}-online-gratis`;
};

export const getGameBySlug = (slug: string) => {
  return games.find(game => getGameSlug(game) === slug);
};

export const getRelatedGames = (currentGame: Game, limit: number = 4) => {
  return games
    .filter(game => 
      game.id !== currentGame.id && 
      (game.category === currentGame.category || 
       game.tags.some(tag => currentGame.tags.includes(tag)))
    )
    .slice(0, limit);
};
